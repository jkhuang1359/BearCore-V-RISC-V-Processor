################################################################
# 最简单的冒烟测试
# 验证最基本的硬件功能
################################################################

.global _start
_start:
    # 发送开始信号
    li t0, 0x10000000
    li t1, 'S'
    sw t1, 0(t0)
    
    # 简单计算
    li x1, 1
    li x2, 2
    add x3, x1, x2
    li x4, 3
    bne x3, x4, fail
    
    # 发送成功信号
    li t1, 'P'
    sw t1, 0(t0)
    ebreak
    
fail:
    li t1, 'F'
    sw t1, 0(t0)
    ebreak
    