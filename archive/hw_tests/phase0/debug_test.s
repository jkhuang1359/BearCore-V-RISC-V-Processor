################################################################
# 調試測試：只測試 UART 輸出
# 如果硬體工作正常，應該輸出 "X"
################################################################

.global _start
_start:
    # 初始化堆棧（可選）
    # lui sp, 0x8
    
    # 🏆 直接輸出字符 'X' 到 UART
    li t0, 0x10000000   # UART 數據寄存器地址
    li t1, 'X'
    sw t1, 0(t0)
    
    # 🏆 再輸出一個換行
    li t1, '\n'
    sw t1, 0(t0)
    
    # 🏆 無限循環
    1: j 1b